/**
 * CP932文字で2バイト文字コードの1バイト目か確認する
 * @param b	入力 MBCS 文字列
 * @return	true 2バイト文字の1バイト目, false 1バイト文字
 */
bool IsSJISLeadByte( tjs_nchar b )
{
	tjs_uint16 ch = SJIS2UNICODE_Submap_map_00[b];
	return ( ch == 0x0000U );
}
