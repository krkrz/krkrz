/**
 * CP932一文字をUNICODEに変換する
 * @param in	入力 MBCS
 * @param out	出力 UNICODE (wchar_t)
 * @return	変換に成功すれば真、失敗すれば偽
 */
static bool inline TVPSJISToUnicode(const char * & in, tjs_char *out)
{
	const unsigned char * & p = (const unsigned char * &)in;

	// 1 byte 文字をチェック
	tjs_uint16 ch = SJIS2UNICODE_Submap_map_00[p[0]];
	if(ch != 0x0000U)
	{
		// 1byte
		if(out) *out = static_cast<tjs_char>(ch);
		in ++;
		return true;
	}

	if(p[0] >= 0x80)
	{
		 const tSJIS2UNICODE_Submap & submap =
		 	SJIS2UNICODE_Submap[p[0]-0x80];
		if(submap.submap && submap.low <= p[1] && submap.high >= p[1])
		{
			ch = submap.submap[p[1]-submap.low];
			if(ch != 0x0000U)
			{
				// 2 byte map found
				if(out) *out = static_cast<tjs_char>(ch);
				in += 2;
				return true;
			}
		}
	}

	// no conversion available
	return false;
}
